/// List of all the paths that are used to navigate in temp feature.
///
/// Have to been filled with the entries like this:
/// ```dart
/// /// Path description.
/// static const String screenPath = 'screenPath';
/// ```
abstract class AppRoutePaths {
  /// Path to hotel screen.
  static const String hotelPath = '/hotel';

  /// Path to room screen.
  static const String roomPath = '/room';

  /// Path to booking screen.
  static const String bookingPath = '/booking';
}
